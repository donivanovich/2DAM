if not (2 == 2 or 1 == 1) and True:         # Evaluacion de condiciones
    print("dentro del if")                  # Si la condicion es verdadera
    print("otra cosa")                      # Otra instruccion dentro del if    

elif 3 == 3:                                # Evaluacion de otra condicion
    print("elif")                           # Si la condicion del elif es verdadera           

else:                                       # Si ninguna condicion anterior es verdadera          
    print("else")                           # Instruccion dentro del else         

print("fuera del if")                       # Instruccion fuera del if